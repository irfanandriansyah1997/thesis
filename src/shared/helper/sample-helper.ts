class SampleHelper {
    /**
     * Add Operator
     * @param {number} number1 - number 1 param
     * @param {number} number2 - number 2 param
     * @return {number}
     */
    public static addNumber(number1: number, number2: number): number {
        return number1 + number2;
    }
}

export default SampleHelper;
